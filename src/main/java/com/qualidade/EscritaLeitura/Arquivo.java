package com.qualidade.EscritaLeitura;

public class Arquivo {
	
	private double numero;
	private double potencia2;
	private double potencia3;

	public Arquivo(Double valueOf, Double valueOf2, Double valueOf3) {
		this.numero = valueOf;
		this.potencia2 = valueOf2;
		this.potencia3 = valueOf3;
	}

	public Double getNumero() {
		return numero;
	}
	
	public Double getPotencia2() {
		return potencia2;
	}
	
	public Double getPotencia3() {
		return potencia3;
	}
	
	

}
